"""
Conversão, tipos de coerção 
type convertion, typecasting, coercion 
é o ato de converte um tipo em outro 
tipos imutáveis e primítivos 
str, int, float, bool 
"""
print('1', type('1'))
print(type(int('1') + 1)) # conersão da var '1' para interiro 
print(type(float('1') + 1)) # conersão da var '1' para float = retorna um número float
print(int('8') + 1)
